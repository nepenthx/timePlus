/**
 * 优先级枚举定义
 *
 * 本文件定义了待办事项的优先级枚举类型。
 * 优先级用于区分待办事项的重要程度和紧急程度，帮助用户合理安排任务顺序。
 *
 * 使用场景：
 * - 在待办列表中按优先级排序显示
 * - 通过不同颜色标识不同优先级的待办
 * - 提醒用户关注高优先级任务
 *
 * @author nepenthx
 * @since 1.0
 */
package com.nepenthx.timer.data

/**
 * 优先级枚举类
 *
 * 定义待办事项的四个优先级级别，从低到高依次为：
 * - LOW: 低优先级，适用于不紧急也不重要的任务
 * - MEDIUM: 中优先级（默认），适用于一般性任务
 * - HIGH: 高优先级，适用于重要或紧急的任务
 * - URGENT: 紧急优先级，适用于需要立即处理的任务
 *
 * 在UI中通常通过不同颜色来区分：
 * - LOW: 绿色/灰色
 * - MEDIUM: 蓝色
 * - HIGH: 橙色
 * - URGENT: 红色
 */
enum class Priority {
    LOW,      // 低优先级
    MEDIUM,   // 中优先级
    HIGH      // 高优先级
}
